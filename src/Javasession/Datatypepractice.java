package Javasession;

public class Datatypepractice {

	public static void main(String[] args) 
	{
		//Datatypes
		//1. Primitive data type-No need to create object
			//a)boolean-True or false
			//b)Numeric-
				//1)character type-char
				//2)Intergral value 
		         		//integer- byte,short,int,long
						//	Floating point-Float and double
		//2.Non primitive data type--need to create object
		//String,Array,class and Interface
		
		//1 byte =8 bits
		byte b=10;
		b=20;
		System.out.println(b);
		//byte b1=128;
		byte b2=-12;
		System.out.println(b2);
		//Short--2 bytes=16 bits
		short s=128;
		s=1000;
		System.out.println(s);//Prints the latest assigned value 1000
		//int--4 bytes-32 bits
		int i=1200;
		System.out.println(i);
		//long--8 bytes-64 bits
		long l=1000000l;
		System.out.println(l);
		
		//char--4 bytes-32 bits
		char c='a';
		char c1='1';
		System.out.println(c);
		//float--4 bytes-32 bits--takes upto 7 decimal values
		float f=10.76f;
		float f1=10f;
		System.out.println(f);
		System.out.println(f1);
		//double--8 bytes-64 bit--takes upto 15 decimal values
		double d=1000.987655;
		double d1=19908;
		System.out.println(d);
		System.out.println(d1);
		
		
		

	}

}
